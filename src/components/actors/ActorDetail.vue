  components: { Spinner },
  data: () => ({
    actor: "",
    knownFor: [],
    credits: [],
    processing: false,
  }),

  mounted() {
    this.fetchActor(this.$route.params.id);
    this.fetchCredits(this.$route.params.id);
  },

  computed: {
    actorImage() {
      let profilePath;
      if (this.actor.profile_path) {
        profilePath = `https://image.tmdb.org/t/p/w300${this.actor.profile_path}`;
      }

      return profilePath;
    },
  },

  methods: {
    async fetchActor(actorId) {
      try {
        this.processing = true;
        const { data } = await this.$http(`/person/${actorId}`);

        this.actor = data;
        this.processing = false;
      } catch (error) {
        console.log(error);
      }
    },
    async fetchCredits(actorId) {
      try {
        this.processing = true;
        const { data } = await this.$http(
          `/person/${actorId}/combined_credits`
        );

        this.knownFor = data.cast.slice(Math.max(data.cast.length - 5, 1));
        this.credits = data.crew.slice(Math.max(data.crew.length - 5, 1));
        this.processing = false;
      } catch (error) {
        console.log(error);
      }
    },

    movieImage(poster) {
      if (poster) {
        const posterPath = `https://image.tmdb.org/t/p/w185${poster}`;
