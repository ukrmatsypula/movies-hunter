      } else {
        return "https://via.placeholder.com/300x450";
      }
    },

    getAge(birthday) {
      return Math.round(
        (new Date().getTime() - new Date(birthday)) /
          (24 * 3600 * 365.25 * 1000)
      );
    },
  },
};
